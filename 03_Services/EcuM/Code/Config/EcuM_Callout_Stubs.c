/**
 * \brief      Provides basic software initializations before starting the Operating System.
 * \param      -
 * \return     -
 */
void EcuM_AL_DriverInitOne(void)
{
   /* Setup all the clocks and initialize all the RAM data sections. Must be the first call. */
   Mcu_Init(&Mcu_gkt_Config);
   Port_Init(&Port_gkt_Config);
   Adc_Init(&Adc_gkt_Cfg);
   Pwm_Init(&Pwm_gkt_Config);
   Uart_gv_Init(&Uart_gkt_Cfg);
   MotorControl_gv_Init();
   Lcd_gv_Init();

}
