/** \brief  Function-like macro for declaring and defining OS tasks.
 *
 * The pre-processor concatenation is used as suggested in the OSEK 2.2.3 specifications page 54. */
#define TASK(taskName)                       void FUNC_ ## taskName(void)

/** \brief  Function-like macro for declaring and defining interrupt service routines. */
#define ISR(isrName)                         void ISR_ ## isrName(void)

/** \brief  Macro which defines the default (and only) OS application mode. */
#define OSDEFAULTAPPMODE                     ((AppModeType) 0U)

