/**
 * \brief      Reads all the analog ECU signals and stores them  for further processing in the SWC core.
 * \param      -
 * \return     -
 */
void Joystick_gv_RteInputUpdate(void)
{
   Rte_Read_In_BtnJoystick(&Joystick_gat_BtnInputValue);
   Rte_Read_In_PotXAxis(&Joystick_gat_PotInputXValue);
   Rte_Read_In_PotYAxis(&Joystick_gat_PotInputYValue);

}

/*
 * \brief Writes all the output RTE buffers with contents from the core processed values.
 * \param -
 * \return -
 *
 */
void Joystick_gv_RteOutputUpdate(void)
{
   Rte_Write_Out_BtnJoystick(&Joystick_gat_BtnOutputValue);
   Rte_Write_Out_PotXAxis(&Joystick_gat_PotOutputXValue);
   Rte_Write_Out_PotYAxis(&Joystick_gat_PotOutputYValue);
   Rte_Write_Out_PotXLcd(&Joystick_gat_PotOutputXDirection);
   Rte_Write_Out_PotYLcd(&Joystick_gat_PotOutputYDirection);
}
